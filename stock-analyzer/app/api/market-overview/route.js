// 한글 종목명 가져오기 (Naver API)
async function getKSreanName(ticker) {
    try {
        const code = ticker.replace(/\.(KS|KQ)$/, '');
        const url = `https://m.stock.naver.com/api/stock/${code}/basic`;
        const response = await fetch(url);
        const data = await response.json();
        return data.stockName || ticker;
    } catch (error) {
        return ticker;
    }
}

export async function GET(request) {
    try {
        const { searchParams } = new URL(request.url);
        // const limit = parseInt(searchParams.get('limit')) || 30;

        // Yahoo Finance에서 데이터 가져오기
        const tickersToFetch = KSSPI_TICKERS;
        const quotes = [];
        const batchSize = 10;

        for (let i = 0; i < tickersToFetch.length; i += batchSize) {
            const batch = tickersToFetch.slice(i, i + batchSize);
            const batchResults = await Promise.all(
                batch.map(async (ticker) => {
                    try {
                        const quote = await yahooFinance.quote(ticker);
                        const KSreanName = await getKSreanName(ticker);

                        const price = quote.regularMarketPrice || 0;
                        const previousClose = quote.regularMarketPreviousClose || price;
                        const change = price - previousClose;
                        const changePercent = previousClose !== 0 ? (change / previousClose) * 100 : 0;
                        const marketCap = quote.marketCap || 0;
                        const volume = quote.regularMarketVolume || 0;

                        return {
                            ticker: ticker.replace(/\.(KS|KQ)$/, ''),
                            name: KSreanName,
                            sector: SECTOR_MAP[ticker] || '기타',
                            price,
                            change,
                            changePercent,
                            marketCap,
                            volume,
                        };
                    } catch (error) {
                        console.warn(`⚠️ ${ticker} 실패:`, error.message);
                        return null;
                    }
                })
            );
            quotes.push(...batchResults);

            // 배치 사이 300ms 대기
            if (i + batchSize < tickersToFetch.length) {
                await new Promise(r => setTimeout(r, 300));
            }
        }

        // null 제거
        const validQuotes = quotes.filter(q => q !== null);

        // 섹터별 그룹화
        const sectors = {};
        validQuotes.forEach(stock => {
            const sector = stock.sector;
            if (!sectors[sector]) {
                sectors[sector] = {
                    name: sector,
                    stocks: [],
                    totalMarketCap: 0,
                    avgChangePercent: 0,
                };
            }
            sectors[sector].stocks.push(stock);
            sectors[sector].totalMarketCap += stock.marketCap;
        });

        // 섹터별 평균 등락률 계산
        Object.values(sectors).forEach(sector => {
            const totalChange = sector.stocks.reduce((sum, s) => sum + s.changePercent, 0);
            sector.avgChangePercent = totalChange / sector.stocks.length;

            // 시가총액 기준 정렬
            sector.stocks.sort((a, b) => b.marketCap - a.marketCap);
        });

        // 배열로 변환 및 시가총액 기준 정렬
        const sectorsArray = Object.values(sectors).sort(
            (a, b) => b.totalMarketCap - a.totalMarketCap
        );

        return NextResponse.json({
            success: true,
            data: sectorsArray,
            timestamp: new Date().toISOString(),
        });

    } catch (error) {
        console.error('Market overview API error:', error);
        return NextResponse.json(
            { success: false, error: error.message },
            { status: 500 }
        );
    }
}